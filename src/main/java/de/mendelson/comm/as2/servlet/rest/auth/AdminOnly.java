package de.mendelson.comm.as2.servlet.rest.auth;

import jakarta.ws.rs.NameBinding;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;
import java.lang.annotation.ElementType;

/**
 * Annotation to mark REST endpoints that require 'admin' super user access only.
 * This is NOT the same as users with ADMIN role - this is specifically for the
 * 'admin' super user account.
 *
 * @author Julian Xu
 */
@NameBinding
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.TYPE, ElementType.METHOD})
public @interface AdminOnly {
}
