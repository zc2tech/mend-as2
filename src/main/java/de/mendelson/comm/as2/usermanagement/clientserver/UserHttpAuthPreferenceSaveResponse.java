package de.mendelson.comm.as2.usermanagement.clientserver;

import de.mendelson.util.clientserver.messages.ClientServerResponse;
import java.io.Serializable;

/**
 * Response after saving HTTP authentication preferences
 */
public class UserHttpAuthPreferenceSaveResponse extends ClientServerResponse {

    private static final long serialVersionUID = 1L;

    private boolean success;
    private String errorMessage;

    public UserHttpAuthPreferenceSaveResponse(UserHttpAuthPreferenceSaveRequest request) {
        super(request);
        this.success = true;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }

    @Override
    public String toString() {
        return "HTTP auth preferences save response: " + (success ? "success" : "failed - " + errorMessage);
    }
}
