package de.mendelson.comm.as2.usermanagement.clientserver;

import de.mendelson.util.clientserver.messages.ClientServerMessage;
import java.io.Serializable;

/**
 * Message to assign a role to a user
 */
public class UserRoleAssignRequest extends ClientServerMessage implements Serializable {

    private static final long serialVersionUID = 1L;
    private int userId;
    private int roleId;

    public UserRoleAssignRequest() {
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getRoleId() {
        return roleId;
    }

    public void setRoleId(int roleId) {
        this.roleId = roleId;
    }

    @Override
    public String toString() {
        return "Assign role " + roleId + " to user " + userId;
    }
}
